#!/usr/bin/env python3
"""Create a bootable floppy image for FeatherOS."""

with open('kernel.bin', 'rb') as f:
    kernel = f.read()

img = bytearray(1440 * 1024)

# Simple boot sector that prints 'FeatherOS boot' and halts
boot = bytearray(512)

# 16-bit real mode code
code = [
    # Print "FeatherOS boot\r\n"
    0xB4, 0x0E, 0xB0, ord('F'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('e'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('a'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('t'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('h'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('e'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('r'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('O'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('S'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord(' '), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('b'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('o'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('o'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, ord('t'), 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, 0x0D, 0xCD, 0x10,
    0xB4, 0x0E, 0xB0, 0x0A, 0xCD, 0x10,
    0xF4,  # HLT
]

for i, b in enumerate(code):
    boot[i] = b

# Padding
for i in range(len(code), 510):
    boot[i] = 0x90

# Boot signature
boot[510] = 0x55
boot[511] = 0xAA

img[0:512] = boot
img[512:512 + len(kernel)] = kernel

with open('featheros.img', 'wb') as f:
    f.write(img)

print(f"Created featheros.img with {len(kernel)} byte kernel")
